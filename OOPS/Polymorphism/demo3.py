# polymorphism with inbuilt methods

#printing length of two different objects
print(len([2, 4, 6, 8, 9]))
print(len("FACE Prep"))

# the method 'len' prints the length of two different objects.
# In the first print statement, it prints the length of a list object and in the second, it prints the length of a string object.
# So, polymorphism is achieved using the built-in method 'len'.